package net.cedu.model.crm;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

/**
 * 任务分配业务实体
 * 
 * @author yangdongdong
 * 
 */
public class CrmUserModel {

	private int userId;// 用户ID
	private String fullName;// 用户真实姓名
	private String userName;// 用户名
	private String email;// 邮件地址
	private String mobile;// 手机号码
	private Date createdTime;// 创建日期
	transient private int assignedCount;// 已分配人数
	transient private int yiyuanCount;// 无意愿人数
	transient private int baomingCount;// 已报名人数
	transient private int followUpAllCount;// 跟进中人数
	transient private int followUpACount;// 跟进中(A)
	transient private int followUpBCount;// 跟进中(B)
	transient private int followUpCCount;// 跟进中(C)
	transient private int followUpDCount;// 跟进中(D)
	transient private int followUpECount;// 跟进中(E)
	transient private int followUpFCount;// 跟进中(F)

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public int getAssignedCount() {
		return assignedCount;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public void setAssignedCount(int assignedCount) {
		this.assignedCount = assignedCount;
	}

	public int getFollowUpAllCount() {
		return followUpAllCount;
	}

	public void setFollowUpAllCount(int followUpAllCount) {
		this.followUpAllCount = followUpAllCount;
	}

	public int getFollowUpACount() {
		return followUpACount;
	}

	public void setFollowUpACount(int followUpACount) {
		this.followUpACount = followUpACount;
	}

	public int getFollowUpBCount() {
		return followUpBCount;
	}

	public void setFollowUpBCount(int followUpBCount) {
		this.followUpBCount = followUpBCount;
	}

	public int getFollowUpCCount() {
		return followUpCCount;
	}

	public void setFollowUpCCount(int followUpCCount) {
		this.followUpCCount = followUpCCount;
	}

	public int getFollowUpDCount() {
		return followUpDCount;
	}

	public void setFollowUpDCount(int followUpDCount) {
		this.followUpDCount = followUpDCount;
	}

	public int getFollowUpECount() {
		return followUpECount;
	}

	public void setFollowUpECount(int followUpECount) {
		this.followUpECount = followUpECount;
	}

	public int getFollowUpFCount() {
		return followUpFCount;
	}

	public void setFollowUpFCount(int followUpFCount) {
		this.followUpFCount = followUpFCount;
	}

	public int getYiyuanCount() {
		return yiyuanCount;
	}

	public void setYiyuanCount(int yiyuanCount) {
		this.yiyuanCount = yiyuanCount;
	}

	public int getBaomingCount() {
		return baomingCount;
	}

	public void setBaomingCount(int baomingCount) {
		this.baomingCount = baomingCount;
	}
	

}
