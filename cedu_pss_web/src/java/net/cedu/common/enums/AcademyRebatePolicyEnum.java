package net.cedu.common.enums;

/**
 * 院校返款政策状态
 * 
 * @author Sauntor
 *
 */
public enum AcademyRebatePolicyEnum
{
	AuditOk(1),  //审批通过
	AuditBad(2), //审批未通过
	Enable(3),   //启用
	Disable(4);  //停用
	
	private final int value;
	
	AcademyRebatePolicyEnum(int v){
		this.value = v;
	}
	
	public int value(){
		return value;
	}
}
